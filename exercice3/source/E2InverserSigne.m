function [inverse] = E2InverserSigne(nombre)
% E2InverserSigne - L'inverse additif d'un nombre.
% Trouve la valeur de du nombre multiplié par -1.
%
% Syntaxe : [inverse] = E2_inverser_signe(nombre)
%
% Entrées :
%   nombre [double] - Le nombre à inverser.
%
% Sorties :
%   inverse [double] - L'inverse du nombre.
%
% Example :
%   E2InverserSigne(3) %% Retourne -3
%

inverse = -nombre;

end
