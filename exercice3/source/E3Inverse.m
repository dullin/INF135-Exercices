function [inverse] = E3Inverse(nombre)
% E3Inverse - L'inverse multiplicatif
% Retourne la valeur à la puissance -1.
%
% Syntaxe : [inverse] = E3Inverse(nombre)
%
% Entrées :
%   nombre [double] - La valeur dont on veut retourner l'inverse.
%
% Sorties :
%   inverse [double] - L'inverse multiplicaitf de la valeur de départ.
%
% Example :
%   E3Inverse(4) %% Retourne 0.25
%

inverse = 1/nombre;

end
